// ============================================================================
// Interview Problem: Linked List Intersection
// ============================================================================
//
// -------
// Prompt:
// -------
//
// Write a function linkedListIntersection that returns the node at which the 
// intersection of two linked lists begins, or null if there is no such 
// intersection.
//
// ---------- 
// Example 1:
// ----------
// 
// Given the following two linked lists, list1 and list2, 
// linkedListIntersection(list1,list2) should return D 
// as the node of intersection.
// 
//    A → B → C
//             ↘
//               D → E → F
//             ↗
//        X → Y
//
// ---------- 
// Example 2:
// ----------
//
// Given the following two linked lists, list1 and list2, 
// linkedListIntersection(list1, list2) should return null 
// as there is no point of intersection.
// 
//    A → B → C → D
//
//    X → Y → Z
// 
// -----------
// Let's code!
// -----------
function linkedListIntersection(list1, list2) {
  // Calculate the length of both lists

  const length1 = getLength(list1.head);  // 5
  const length2 = getLength(list2.head);  // 3

  // Find the difference (diff)

  let diff = Math.abs(length2 - length1)  // 2

  // Move the longer list ahead by diff 

  let long = length1 >= length2 ? list1.head : list2.head;
  let short = length1 <= length2 ? list1.head : list2.head;

  while (diff) {
    long = long.next;
    diff--;
  }

  // Step along both lists at same pace until 
  // we find the spot where head1 = head2

  while (long && short) {
    if (long === short) return long;
    long = long.next;
    short = short.next;
  }

  return null;
}

function getLength(node) {
  if (node.next === null) return 1;
  return getLength(node.next) + 1;
}

// ----------------------------------------
// Given: Singly Linked List - Do Not Edit!
// ----------------------------------------
class Node {
  constructor(val) {
    this.value = val;
    this.next = null;
  }
}

class LinkedList {
  constructor() {
    this.head = null;
    this.tail = null;
    this.length = 0;
  }

  addToTail(val) {
    const newNode = new Node(val);

    if (!this.head) {
      this.head = newNode;
    } else {
      this.tail.next = newNode;
    }

    this.tail = newNode;
    this.length++;
    return this;
  }

  get(index) {
    if (index < 0 || index >= this.length) return null;
    let counter = 0;
    let current = this.head;
    while (counter !== index) {
      current = current.next;
      counter++;
    }
    return current;
  }
}

